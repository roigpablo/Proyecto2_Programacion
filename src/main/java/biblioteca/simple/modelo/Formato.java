package biblioteca.simple.modelo;

// Un enum (enumeración) es un tipo especial que representa un conjunto fijo de valores constantes.
// En este caso, "Formato" define los tipos posibles que puede tener un Producto (Libro, Película, etc.)
public enum Formato {
    FISICO,   // Ejemplos: libro en papel, DVD, revista impresa...
    DIGITAL   // Ejemplos: eBook, PDF, película online, audiolibro...
}
